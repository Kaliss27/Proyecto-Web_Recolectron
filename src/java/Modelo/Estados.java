
package Modelo;

/**
 *
 * @author Alvaro
 */
public class Estados {
    int id;
    String estado;

    public Estados() {
    }

    public Estados(String estado) {
        this.estado = estado;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }
    
    
}
